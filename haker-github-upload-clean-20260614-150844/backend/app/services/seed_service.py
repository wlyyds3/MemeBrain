def ensure_runtime_schema() -> None:
    inspector = inspect(engine)
    if "knowledge_items" not in inspector.get_table_names():
        return

    existing_columns = {column["name"] for column in inspector.get_columns("knowledge_items")}
    alter_statements = {
        "work_title": "ALTER TABLE knowledge_items ADD COLUMN work_title VARCHAR(255)",
        "scene_title": "ALTER TABLE knowledge_items ADD COLUMN scene_title VARCHAR(255)",
        "scene_description": "ALTER TABLE knowledge_items ADD COLUMN scene_description TEXT",
        "episode_label": "ALTER TABLE knowledge_items ADD COLUMN episode_label VARCHAR(120)",
        "timestamp_start": "ALTER TABLE knowledge_items ADD COLUMN timestamp_start VARCHAR(30)",
        "timestamp_end": "ALTER TABLE knowledge_items ADD COLUMN timestamp_end VARCHAR(30)",
        "speaker": "ALTER TABLE knowledge_items ADD COLUMN speaker VARCHAR(120)",
        "quote_text": "ALTER TABLE knowledge_items ADD COLUMN quote_text TEXT",
        "quote_verified": "ALTER TABLE knowledge_items ADD COLUMN quote_verified BOOLEAN NOT NULL DEFAULT 0",
        "concepts": "ALTER TABLE knowledge_items ADD COLUMN concepts TEXT",
        "characters": "ALTER TABLE knowledge_items ADD COLUMN characters TEXT",
    }

    with engine.begin() as connection:
        for column_name, statement in alter_statements.items():
            if column_name not in existing_columns:
                connection.execute(text(statement))


def seed_knowledge_items() -> None:
    with SessionLocal() as db:
        existing_titles = set(db.scalars(select(KnowledgeItem.title)).all())
        for item in SEED_ITEMS:
            if item["title"] in existing_titles:
                continue
            db.add(KnowledgeItem(**item))
        db.commit()


def seed_default_admin() -> None:
    with SessionLocal() as db:
        existing = db.scalar(select(User).where(User.email == settings.default_admin_email))
        if existing:
            return

        admin = User(
            email=settings.default_admin_email,
            name=settings.default_admin_name,
            password_hash=get_password_hash(settings.default_admin_password),
        )
        db.add(admin)
        db.commit()
